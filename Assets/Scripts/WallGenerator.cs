#if UNITY_EDITOR
    [ContextMenu("生成 3D 地图")]
    public void GenerateMap()
    {
        foreach (Transform child in transform)
        {
            DestroyImmediate(child.gameObject);
        }

        for (int x = 0; x < mapTemplate.GetLength(0); x++)
        {
            for (int z = 0; z < mapTemplate.GetLength(1); z++)
            {
                Vector3 position = new Vector3(x, 0, z);

                if (mapTemplate[x, z] == 1)
                {
                    GameObject wall = (GameObject)PrefabUtility.InstantiatePrefab(wallPrefab);
                    wall.transform.position = new Vector3(x - 24.5f, 0.5f, z - 24.5f); // 墙体高出地面
                    wall.transform.SetParent(this.transform);
                    wall.name = $"Wall_{x}_{z}";
                }
            }
        }
    }
#endif
